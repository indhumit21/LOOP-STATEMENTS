class Charexample
{
	public static void main(String[] args)
	{
		int val='A';
		switch(val) //int or char or string
		{
			case 'A':System.out.println("America");
			break;
			
			case 'K':System.out.println("Korea");
			break;
			
			case 'L':System.out.println("London");
			break;
			
			case 'C':System.out.println("Canada");
			break;
			
			default:
			System.out.println("Invalid India");
		}
	}
	
}